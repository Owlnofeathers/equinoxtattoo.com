							<h2 class="page-header"><i class="fa fa-plus-square"></i> Aftercare</h2>	
								<h3>What can I do to be prepared for my appointment?</h3>
								<h4>We want your tattoo experience to be as enjoyable as possible; as such, here is a short list of recommendations to make the process easier on you.</h4>
								<ul>
									<li>Be as well rested as possible.</li>
									<li>Be well hydrated</li>
									<li>Be well fed. Eating a large, solid meal before getting tattooed is very helpful.</li>
									<li>Don't drink or party hard the night before. Getting tattooed hung over is no fun.</li>
									<li>Don't be drunk or on drugs.</li>
									<li>Dress comfortably, and in clothing that easily facilitates access to the area of your body where your tattoo will be placed.</li>
									<li>Bring a care kit for yourself of snacks, a non-alcoholic drink with a lid, and maybe a small blanket or jacket in case you get cold. A distraction like a book or charged phone is helpful for many clients; don't forget to bring headphones if you'd like to listen to your own music!</li>
								</ul>
								<h3>How do I care for my new tattoo?</h3>
								<p>Proper aftercare is essential to your health and making sure your tattoo looks beautiful for years to come, and is a simple addition to your daily routine as your tattoo heals.  Always wash your hands with liquid antibacterial soap before cleaning your tattoo. The bandage your artist affixes at the end of your appointment should be left on for a minimum of 3-4 hours, and can be left on overnight.  After removing your bandage, gently rinse the tattoo with warm water and mild antibacterial soap. After the surface of your tattoo is clean, either let it air dry or pat it dry with a clean paper towel.  Once the tattooed area is completely dry, apply a THIN layer of unscented, unmedicated lotion or ointment.  The amount of times this process needs to be repeated daily can vary wildly depending on your skin; keep your tattoo:</p>
								<ul>
									<li>clean (no dirt, debris, sweat, etc.).</li>
									<li>dry (don't soak it any bodies of water, no heavy sweating, nothing that will saturate and soften the thin scab protecting your healing skin).</li>
									<li>lightly moisturized (enough lotion or ointment to keep the healing skin comfortable and free from cracking, but not enough to saturate and soften the scab protecting your healing skin).</li>
								</ul>
								<h3>Other tips for an easy healing process:</h3>
								<ul>
									<li>Change your sheets - While you sleep you shed skin, sweat and hair. Anything in your bed can come in contact with your tattoo and increase the chances of infection.</li>
									<li>Do not use bar soap - Bar soap is not sterile, can hold bad bacteria and can pass bacteria on to your tattoo. Gross. Invest in a small disposable bottle of antibacterial liquid soap.</li>
									<li>Application of ointment - Believe it or not, your hands are not clean. After you wash your hands, anything you touch afterwards (door handles, cups, hair, face, the list goes on...) has bacteria on it and therefore your hands become contaminated. You must wash your hands (thoroughly) and your tattoo (gently) with warm water and soap before applying any ointment to your tattoo.</li>   
									<li>Pets - They track harmful bacteria everywhere they walk, lie, or brush against. Wash and reapply ointment after contact with animals. This also applies to anything the animal comes in contact with such as sheets, furniture and clothing.</li>
									<li>Sweating - When you sweat you release toxins which sit on the surface of your tattoo, increasing the chances of infection. After heavy sweating, you must wash your hands (thoroughly) and your tattoo (gently) with warm water and soap and reapply ointment to your tattoo.</li>
									<li>Do not wear restrictive clothing - If your tattoo forms a scab do not let it stick to anything such as sheets or tight clothing. A wet scab will stick to anything it dries against. When you pull that item away (sheets or clothing) the scab will pull off with it. This pulls ink out of the skin and can cause scarring and loss of color.</li>
								</ul>
								<h3>How long will it take my tattoo to heal?</h3>
								<p>Your healing time will vary depending various internal immune factors and the amount of skin broken during the tattoo process.  A month is generally more than enough time for the skin damaged during the tattoo process to fully heal.  A general timeline for healing is as follows.</p>
								<ul>
									<li>The tattoo is considered "fresh" and vulnerable to infection for about the first three days. During this time period the skin in the area of the tattoo is open and without protection. Your tattoo may be sore and slightly swollen and red during this time. It may also be slightly weepy. It is especially important during this time period to keep your tattoo as clean and dry as possible and avoid any activities that may cause it to come in contact with unclean surfaces. For instance, if you have a recent lower arm tattoo, you have a free get-out-of-doing-the-dishes pass for a few days. While some weeping of the tattoo is normal, especially for the first day, if it continues or the tattoo becomes abnormally red or warm to the touch, please contact your artist promptly.</li>
									<li>Between days 3-7, your tattoo should look about the same as it did fresh, although it may start to appear gradually drier and duller. Swelling, redness and sensitivity should decrease dramatically during this time period.</li>
									<li>Somewhere between days 7-14 your tattoo will start to peel. Pieces of dry, dead skin saturated in tattoo ink will come off the tattoo, and it will look pretty ugly for a while. This is normal. Just continue to care for the tattoo as usual and after about 2-4 days the peeling should stop. This is also just about the time when the tattoo will often become very itchy. While this is an unpleasant side effect of your skin growing back, it is also completely normal and you should do your best to ignore it. Scratching or picking at the tattoo will damage it so do your best to resist.</li>
									<li>Days 14 - 30. After peeling, your tattoo will look fairly healed. At this point as long as the tattoo is peeled and scab free you can step washing down from twice to once a day, although you should continue with sparing moisturizer application for the remainder of the month.</li>
									<li>Day 30: Congratulations, your tattoo should be pretty well healed at this point. Since the tattoo should be fairly settled now would be a great time to check out your tattoo and see if everything healed the way you wanted it to. If the tattoo was damaged during the healing process or something didn't turn out quite the way you thought make sure to contact your artist to schedule a touch up on the piece. Touch ups are usually free within the first two months (unless you willfully or egregiously damaged the tattoo during the healing process or want to add something that wasn't in the original design) so make sure to contact your artist with any concerns.</li>
									<li>And finally, if you feel your tattoo is not healing normally at any point, <a href="artists.php" data-toggle="tooltip" data-placement="top" title="Artists | Equinox Tattoo Collective">contact your artist</a> directly and they will get back to you as quickly as possible. If you are having trouble contacting your artist, please call <a href="contact.php" data-toggle="tooltip" data-placement="top" title="Contact | Equinox Tattoo Collective">the shop number</a> for prompt advice.</li>
								</ul>
